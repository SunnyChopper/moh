	<script type="text/javascript">
		@if(Auth::guest())
		var is_guest = true;
		var user_id = 0;
		var user_email = '';
		@else
		var is_guest = false;
		var user_id = {{ Auth::id() }};
		var user_email = '{{ Auth::user()->email }}';
		@endif
		var email_taken = false;
		var _token = '{{ csrf_token() }}';

		/* ------------------- *\
			Helper Functions
		\* ------------------- */

		function checkRegisterFields() {
			if (email_taken == true) {
				$(".submit_button").prop('disabled', 'disabled');
			} else {
				$(".submit_button").removeAttr('disabled');
			}
		}

		/* ------------------- *\
			Text Bindings
		\* ------------------- */

		$("#register_email").on('change', function() {
			$('#submit_button').prop('disabled', true);
			$.ajax({
				url : '/api/leads/email/check',
				type : 'GET',
				data : {
					'landing_page_name' : 'Habit Tracker',
					'email' : $("#register_email").val()
				},
				success : function(data) {
					if (data == false) {
						$('#submit_button').prop('disabled', false);
						$("#register_email").css('border', '1px solid red');
						$("#register_email_feedback").show();
						email_taken = true;
						checkRegisterFields();
					} else {
						$('#submit_button').prop('disabled', false);
						$("#register_email").css('border', '1px solid green');
						$("#register_email_feedback").hide();
						email_taken = false;
						checkRegisterFields();
					}
				}
			})
		});

		/* ------------------- *\
			Button Bindings
		\* ------------------- */

	

		$(".submit_button").on('click', function() {
			if ($("#register_email").val() != "" && email_taken != true) {
				var email = $("#register_email").val();

				$.ajax({
					url : '/api/leads/submit',
					type : 'POST',
					data : {
						'_token' : _token,
						'email' : email,
						'landing_page_name' : 'Habit Tracker'
					}, 
					success : function(data) {
						if (data == true) {
							$('#submit_success').show();
							$('#register_email').val('');
							$('#register_email').css('border', '1px solid green');
						} else {
							$("#submit_error").html("Something went wrong while submitting...");
							$("#submit_error").show();
						}
					}
				});
			} else {
				$("#submit_error").html("Please fill out required field.");
				$("#submit_error").show();
			}
		});


		/* ------------------- *\
			General
		\* ------------------- */

		$(document).ready(function() {

		});

		$(window).on('scroll', function() { 
			var scrollPos = $(window).scrollTop();
			if(scrollPos < 20) {
				$("#mobile-bottom").css('bottom', '-3.5em');
			} else {
				$("#mobile-bottom").css('bottom', '0');
			}
		});
